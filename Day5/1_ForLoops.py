# Syntax
# for <variable name of each item> in <a List>:
#     <do something>
#     <do something else>

# Predict what will be printed from the code below:
fruits = ["Apple", "Peach", "Pear"]
for fruit in fruits:
    print(fruit)
    print(fruit + " pie")
# Apple Applepie Peach Peachpie Pear Pearpie

